#include "Statistics.h"


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////* INITIALIZATION *////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


Statistics::Statistics(std::string string_)
{
	name = string_;
	time_start = sclock::now();
}

Statistics::~Statistics()
{
	time_end = sclock::now();
	std::cout << name << " duration was " << GetDuration() << " seconds.\n";
	std::cout << name << " average FPS was " << GetAverageFPS() << " frames.\n";
}


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////* GETTERS *////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


int Statistics::GetAverageFPS()
{
	int avgfps = framecount / GetDuration();

	return avgfps;
	
}

int Statistics::GetDuration()
{
	time_end = sclock::now();

	int duration = std::chrono::duration_cast<std::chrono::seconds>(time_end - time_start).count();

	return duration ? duration : -1;
}